# Salto de linea: \n
texto = "Linea1\nLinea2\nLinea3\nLinea4"
print(texto)

# Tabulacion: \t
texto = "Nombre:\tJuan"
print(texto)

# Comilla simple: \'
texto = "Esto es comilla simple:\'"
print(texto)

# Comilla doble: \""
texto = "Esto es comilla doble:\""
print(texto)

# contrabarra: \
texto = "Esto es una contrabarra:\\"
print(texto)

# Retorno: \r
texto = "Hola\rMundo cruel"
print(texto)

# Retroceso: \b
texto = "Hola\rMundo cruel"
print(texto)

# Unicode: \u
texto = "\u03A9"
print(texto)
